plt.subplot(1, 2, 2)
tree.plot_tree(clf_entropy, feature_names=x.columns.tolist(), class_names=['Not Approved', 'Approved'], filled=True, fontsize=9) # Corrected plot_tree call
plt.title("Decision Tree - Loan Status Prediction (Entropy)")
